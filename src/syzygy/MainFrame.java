    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        Food currentFood = selectedFood;
        ArrayList<Ingredient> ingredients = new ArrayList<>();
        for (int i = 0; i < selectedFoodIngredientTable.getRowCount(); i++) {
            ingredients.add(Ingredient.getFromPool(selectedFoodIngredientTable.getValueAt(i, 0).toString()));
        }
        ArrayList<Topping> toppings = new ArrayList<>();
        for (int i = 0; i < selectedFoodToppingTable.getRowCount(); i++) {
            toppings.add(Topping.getOneFromPool(selectedFoodToppingTable.getValueAt(i, 0).toString()));
        }
        int qty = Integer.parseInt(foodQtyCustomerTextField.getText().toString());
        int price = Integer.parseInt(itemPriceLabel.getText().toString());
        ArrayList<ToppingContext> toppingContexts = new ArrayList<>();
        for (int i = 0; i < selectedFoodCustomizationTable.getRowCount(); i++) {
            ToppingContext context = new ToppingContext();
            context.setText(selectedFoodCustomizationTable.getValueAt(i, 0).toString());
            toppingContexts.add(context);
        }
        FoodItem foodItem = FoodItem.Builder().setFood(currentFood).setToppingContexts(toppingContexts).setIngredients(ingredients).setToppings(toppings).setQty(qty).setPrice(price).build();
        foodItems.add(foodItem);
        calculateFoodItemPrice();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton7MouseClicked

