    private void OrderItemAdminTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OrderItemAdminTableMouseClicked
        Order order = OrderManagementGroup.getOrder(Integer.parseInt(OrderAdminTable.getValueAt(OrderAdminTable.getSelectedRow(), 0).toString()));
        FoodItem currentSelectedfoodItem = null;
        for (FoodItem action : order.getFoodItems()) {
            if (action.getFood().getFoodName().equals(OrderItemAdminTable.getValueAt(OrderItemAdminTable.getSelectedRow(), 0).toString())
                    && action.getQty() == Integer.parseInt(OrderItemAdminTable.getValueAt(OrderItemAdminTable.getSelectedRow(), 1).toString())
                    && action.getPrice() == Integer.parseInt(OrderItemAdminTable.getValueAt(OrderItemAdminTable.getSelectedRow(), 2).toString())) {
                currentSelectedfoodItem = action;
                break;
            }
        }
        DefaultTableModel orderItemIngredientTableModel = (DefaultTableModel) orderItemIngredientTable.getModel();
        orderItemIngredientTableModel.setRowCount(0);
        currentSelectedfoodItem.getIngredients().forEach(action -> {
            orderItemIngredientTableModel.addRow(new Object[]{action.getIngredientName()});
        });
        DefaultTableModel orderItemToppingTableModel = (DefaultTableModel) orderItemToppingTable.getModel();
        orderItemToppingTableModel.setRowCount(0);
        currentSelectedfoodItem.getToppings().forEach(action -> {
            orderItemToppingTableModel.addRow(new Object[]{action.getToppingName()});
        });
        // TODO add your handling code here:
    }//GEN-LAST:event_OrderItemAdminTableMouseClicked

