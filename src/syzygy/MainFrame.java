    private void calculateFoodItemPrice() {
        int ingredientValue = 0;
        for (int i = 0; i < selectedFoodIngredientTable.getRowCount(); i++) {
            ingredientValue += Ingredient.getFromPool(selectedFoodIngredientTable.getValueAt(i, 0).toString()).getIngredientPrice();
        }
        for (Ingredient ingredient : selectedFood.getIngredients()) {
            ingredientValue -= ingredient.getIngredientPrice();
        }
        int topppingsValue = 0;
        for (int i = 0; i < selectedFoodToppingTable.getRowCount(); i++) {
            topppingsValue += Topping.getOneFromPool(selectedFoodToppingTable.getValueAt(i, 0).toString()).getToppingPrice();
        }
        int totalFoodItemValue = (ingredientValue + topppingsValue + selectedFood.getFoodPrice()) * Integer.parseInt(foodQtyCustomerTextField.getText().length() > 0 ? foodQtyCustomerTextField.getText() : "0");
        itemPriceLabel.setText(Integer.toString(totalFoodItemValue));
    }

